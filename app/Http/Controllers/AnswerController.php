    {
        $data = $request->validate([
            'questions_id' => 'required',
            'response' => 'required'
        ]);
        Response::create([
            'user_id' => auth()->id(),
            'question_id' => $data['question_id'],
            'response' => $data['response']
        ]);
        return back()->with(['success' => 'created with success']);
    }

